char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	i1;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	i1 = 0;
	while (src[i1] != '\0')
	{
		dest[i] = src[i1];
		i++;
		i1++;
	}
	dest[i] = '\0';
	return (dest);
}
